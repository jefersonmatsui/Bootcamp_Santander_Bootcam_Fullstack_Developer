// 1. O que é DOM?
/*
DOM - Document Object Model

- O DOM HTML é um padrão de como acessar e modificar os elementos HTML de uma página.

- Document;
  - Root element <html>;
    - Element <head>;
    - Element <title>;
    - Text "My title";
  - Element <body>;
    - Element <a>;
      - Attribute "href";
      - Text "My link";
    - Element <h1>;
      - Text "My header";
    
*/

// 2. DOM vs BOM
/*
BOM - Browser Object Model
* Tudo o que está dentro do objeto window

- window;
  - document;
  - history;
  - location;
  - screen;
  - navigator;
*/