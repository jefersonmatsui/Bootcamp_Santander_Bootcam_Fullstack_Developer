// 2.1. Abstração
/*
"Processo mental que consiste em isaolar um aspecto determinado de um estado de coisas relativamente complexo,
a fim de simplificar a asua avaliação, classificação ou para permitir a comunicação do mesmo."
*/

// 2.2. Herança
/*
O objeto filho herda propriedades e métodos do objeto pai
*/

// 2.3. Encapsulamento
/*
Cada classe tem propriedades e métodos independentes do restante do código
*/

// 2.4. Polimorfismo
/*
Objetos podem herdar a mesma classe pai, mas se comportarem de forma diferente quando invocamos seus métodos
*/
